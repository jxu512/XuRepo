package demo.sprintbootweb;

public class Rewards {
	
	private String name;
	private int points;
	
	public Rewards() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getpoints() {
		return points;
	}

	public void setpoints(int points) {
		this.points = points;
	}
	
}
